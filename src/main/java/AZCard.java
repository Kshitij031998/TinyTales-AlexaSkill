package main.java;

public class AZCard {

	String smallImageUrl;
	String largeImageUrl;
	String title;
	String body;
	
	AZCard(String smallImageUrl, String largeImageUrl, String title, String body){
		this.smallImageUrl = smallImageUrl;
		this.largeImageUrl = largeImageUrl;
		this.title = title;
		this.body = body;
	}
	
}
