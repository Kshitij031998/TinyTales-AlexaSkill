package main.java;

public class AZCard {

	public String smallImageUrl;
	public String largeImageUrl;
	public String title;
	public String body;
	
	AZCard(String smallImageUrl, String largeImageUrl, String title, String body){
		this.smallImageUrl = smallImageUrl;
		this.largeImageUrl = largeImageUrl;
		this.title = title;
		this.body = body;
	}
	
}
